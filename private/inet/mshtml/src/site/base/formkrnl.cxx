BOOL
IsValidAccessKey(CDoc * pDoc, CMessage * pmsg)
{
    BOOL fResult =  (pmsg->message == WM_SYSKEYDOWN)
                 || (pDoc->_fInHTMLDlg && pmsg->message == WM_CHAR);
    if (fResult)
    {
        fResult =  (pmsg->wParam != VK_MENU)
                && (pDoc->_aryAccessKeyItems.Size() > 0);
    }
    return fResult;
}


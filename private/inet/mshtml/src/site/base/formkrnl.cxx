{
    CDoc *pDoc = this;
    while (pDoc->_pDocParent)
    {
        if (!pDoc->_fTrustedDoc && pDoc->_pDocParent->_fTrustedDoc)
            break;
        pDoc = pDoc->_pDocParent;
    }
    return pDoc;
}

